#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (lst == NULL || del == NULL)
		return ;
	(*del)(lst -> value);
	free (lst);
}
/*This function takes a list element as parameter and deletes its content
as well as free the allocated memory.*/